    private void acceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptBtnActionPerformed
       try {
            this.acceptBtn.setEnabled(false);
            DatabaseHandler dbHandler = new DatabaseHandler();
            String user = this.userInput.getText();
            String password = this.passwordInput.getText();
            String profile = dbHandler.getUserProfile(user, password);
            this.displayViewForProfile(profile);
        } catch (SQLException ex) {
            this.showError("Error cargando datos");
        }
        finally{
            this.acceptBtn.setEnabled(true);
        }
    }//GEN-LAST:event_acceptBtnActionPerformed

