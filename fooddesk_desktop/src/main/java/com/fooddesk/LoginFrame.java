    private void displayViewForProfile(String profile){
        if (profile == null){
            this.showError("Nombre de usuario o password incorrectos");
            return;
        }
        
        switch(profile.toLowerCase()){
            case "cliente":
                CustomerFrame customerFrame = new CustomerFrame();
                this.dispose();
                customerFrame.setVisible(true);
                break;
            case "administrador":
                AdministrationFrame adminFrame = new AdministrationFrame();
                this.dispose();
                adminFrame.setVisible(true);
                break;
            case "empleado":
                WorkerFrame workerFrame = new WorkerFrame();
                this.dispose();
                workerFrame.setVisible(true);
                break;
            default:
                this.acceptBtn.setEnabled(true);
                this.showError("El perfil de tu usuario no es valido");
                break;
        }
    }
    
